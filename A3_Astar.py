import heapq

def heuristic(node, e1, e2):
  return min(abs(node[0] - e1[0]) + abs(node[1] - e1[1]), abs(node[0] - e2[0]) + abs(node[1] - e2[1]))

def a_star(matrix, s, e1, e2):
  m = len(matrix)
  n = len(matrix[0])

  moves_list = [(0,1), (0,-1), (1,0), (-1,0)]

  visited = set()
  path_list = [s]

  g_cost = {}
  g_cost[s] = 0
  # (heuristic, cur_node)
  heap = []

  heapq.heappush(heap, (heuristic(s, e1, e2), s))

  while heap:
    node = heapq.heappop(heap)[1] # return node

    if node == e1:
      print("Found exit 1")
      print("Path List: ", path_list)
      print("Cost: ", len(path_list))
      return True
    if node == e2:
      print("Found exit 2")
      print("Path List: ", path_list)
      print("Cost: ", len(path_list))
      return True

    visited.add(node)

    for i, j in moves_list:
      next_node = (node[0] + i, node[1] + j)

      if 0 <= next_node[0] < m and 0 <= next_node[1] < n and matrix[next_node[0]][next_node[1]] != 1 and next_node not in visited:
        new_g_cost = g_cost[node] + heuristic(node, e1, e2)
      
        inHeap = False
        for item in heap:
          if item[1] == next_node:
            inHeap = True

        if new_g_cost < g_cost.get(next_node, 0) or not inHeap:
          g_cost[next_node] = new_g_cost

          f_score = new_g_cost + heuristic(next_node, e1, e2)
          heapq.heappush(heap, (f_score, next_node))

          path_list.append(next_node)

  print("No exit found!")  
  return False

