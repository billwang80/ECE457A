import collections

def bfs(matrix, s, e1, e2, tiebreak_order):
  m = len(matrix)
  n = len(matrix[0])

  q = collections.deque()
  q.append((s[0], s[1]))

  visited = set()
  path_list = []
  cost = 0

  while q:
    node = q.popleft()

    if node == e1:
      print('Found exit 1')
      print('Cost:', cost)
      print('Path List:', path_list)
      return True
    elif node == e2:
      print('Found exit 2')
      print('Cost:', cost)
      print('Path List:', path_list)
      return True

    if node[0] >= 0 and node[0] < m and node[1] >= 0 and node[1] < n and matrix[node[0]][node[1]] != 1 and node not in visited:
      if tiebreak_order == 0:
        q.append((node[0] - 1, node[1]))
        q.append((node[0], node[1] + 1))
        q.append((node[0] + 1, node[1]))
        q.append((node[0], node[1] - 1))
      elif tiebreak_order == 1:
        q.append((node[0] + 1, node[1]))
        q.append((node[0], node[1] + 1))
        q.append((node[0] - 1, node[1]))
        q.append((node[0], node[1] - 1))

      cost += 1
      visited.add(node)
      path_list.append(node)

  print('Failed to find exit')
  return False

