import collections

def dfs(matrix, s, e1, e2, tiebreak_order):
  m = len(matrix)
  n = len(matrix[0])

  visited = set()
  path_list = []
  cost = 0

  def helper(node, visited, cost):
    if node == e1:
      print("Found exit 1")
      print(path_list)
      print(cost)
      return True
    if node == e2:
      print("Found exit 2")
      print(path_list)
      print(cost)
      return True

    has_found_exit = False

    if node[0] >= 0 and node[0] < m and node[1] >= 0 and node[1] < n and matrix[node[0]][node[1]] != 1 and node not in visited:
      cost += 1
      visited.add(node)
      path_list.append(node)
      
      if tiebreak_order == 0:
        if helper((node[0] - 1, node[1]), visited, cost): return True
        if helper((node[0], node[1] + 1), visited, cost): return True
        if helper((node[0] + 1, node[1]), visited, cost): return True
        if helper((node[0], node[1] - 1), visited, cost): return True

      elif tiebreak_order == 1:
        if helper((node[0] + 1, node[1]), visited, cost): return True
        if helper((node[0], node[1] + 1), visited, cost): return True
        if helper((node[0] - 1, node[1]), visited, cost): return True
        if helper((node[0], node[1] - 1), visited, cost): return True
    
    return has_found_exit

  if not helper(s, visited, cost):
    print("Not found exit")
    return False
  return True

